while True:
    user_input = input("옵션을 골라주세요\n")

    if user_input == "1":
        print("1번 옵션을 골랐습니다.\n\n")
    elif user_input == "2":
        print("2번 옵션을 골랐습니다.\n\n")
    elif user_input == "3":
        print("3번 옵션을 골랐습니다.\n\n")
        break
    else:
        print("옵션은 1,2,3,4번까지 있습니다.\n\n")